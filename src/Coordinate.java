/**
 * Koordináták tárolására szolgáló osztály.
 */
public class Coordinate {
    public int x;
    public int y;

    /**
     * Konstruktor.
     * @param x Az x koordináta
     * @param y Az y koordináta
     */
    public Coordinate(int x, int y) {
        this.x = x;
        this.y = y;
    }
}
